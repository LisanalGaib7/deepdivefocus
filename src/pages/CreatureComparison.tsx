const SAMPLES = [sardine, crab, barreleye];

const Renderer = ({ rows, colors, glow, size }: { rows: string[]; colors: Record<string, string>; glow: string; size: number }) => {
  const grid = parse(rows);
  const n = grid.length;
  return (
    <svg
      viewBox={`0 0 ${n} ${n}`}
      width={size}
      height={size}
      style={{
        filter: `drop-shadow(0 0 8px ${glow}) drop-shadow(0 0 16px ${glow}66)`,
        imageRendering: "pixelated",
      }}
    >
      {grid.flatMap((row, y) =>
        row.map((cell, x) =>
          cell !== "." ? (
            <rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={colors[cell] ?? "#f0f"} />
          ) : null
        )
      )}
    </svg>
  );
};

const RESOLUTIONS = [16, 32, 48] as const;
type Res = typeof RESOLUTIONS[number];

const CreatureComparison = () => {
  const [active, setActive] = useState<Res | "all">("all");
  const [displayPx, setDisplayPx] = useState(192);

  // Dev-only page. In production builds this renders nothing (route still exists but is inert).
  if (!import.meta.env.DEV) return null;

  const getRows = (s: Sample, r: Res) => (r === 16 ? s.g16 : r === 32 ? s.g32 : s.g48);

  return (
    <div className="min-h-screen bg-background text-foreground p-6 font-mono">
      <div className="max-w-6xl mx-auto">
        <h1 className="text-3xl font-bold tracking-widest mb-2 text-primary">
          PIXEL RESOLUTION COMPARISON
        </h1>
        <p className="text-sm text-muted-foreground mb-6">
          Same rendered size, different internal grid resolution. Pick the one that reads best on the actual collection cell size.
        </p>

        <div className="flex flex-wrap gap-3 mb-6 items-center">
          <div className="flex gap-2">
            <button
              onClick={() => setActive("all")}
              className={`px-4 py-2 rounded border ${active === "all" ? "bg-primary text-primary-foreground" : "border-border"}`}
            >
              ALL
            </button>
            {RESOLUTIONS.map((r) => (
              <button
                key={r}
                onClick={() => setActive(r)}
                className={`px-4 py-2 rounded border ${active === r ? "bg-primary text-primary-foreground" : "border-border"}`}
              >
                {r}×{r}
              </button>
            ))}
          </div>
          <div className="flex items-center gap-2 ml-auto">
            <label className="text-xs text-muted-foreground">Display size</label>
            <input
              type="range"
              min={64}
              max={384}
              step={16}
              value={displayPx}
              onChange={(e) => setDisplayPx(parseInt(e.target.value))}
            />
            <span className="text-xs w-12">{displayPx}px</span>
          </div>
        </div>

        <div className="space-y-12">
          {SAMPLES.map((s) => {
            const resList = active === "all" ? RESOLUTIONS : [active];
            return (
              <div key={s.name} className="border border-border rounded-lg p-6 bg-card">
                <h2 className="text-xl font-bold mb-4 tracking-wider">{s.name}</h2>
                <div className="flex flex-wrap gap-8 items-end">
                  {resList.map((r) => (
                    <div key={r} className="flex flex-col items-center gap-2">
                      <Renderer
                        rows={getRows(s, r)}
                        colors={s.colors}
                        glow={s.glow}
                        size={displayPx}
                      />
                      <div className="text-xs text-muted-foreground tracking-widest">
                        {r}×{r}
                      </div>
                    </div>
                  ))}
                </div>
              </div>
            );
          })}
        </div>

        <div className="mt-8 text-xs text-muted-foreground border-t border-border pt-4">
          <p>Tip: Bestiary cells on mobile are ~80px. Drag the slider to 80 to preview real collection size.</p>
        </div>
      </div>
    </div>
  );
};

export default CreatureComparison;
